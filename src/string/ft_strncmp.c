#include "libft.h"

int ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (n--)
	{
		if (*s1 != *s2++)
			return (*s1 - *--s2);
		if (!*s1++)
			break ;
	}
	return (0);
}
