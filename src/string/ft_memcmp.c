#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const t_u8	*ptr1;
	const t_u8	*ptr2;

	ptr1 = s1;
	ptr2 = s2;
	while (n--)
		if (*ptr1++ != *ptr2++)
			return (*--ptr1 - *--ptr2);
	return (0);
}
