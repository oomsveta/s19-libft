#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	t_u8 *s;
	t_u8 *d;

	s = (t_u8 *)src;
	d = (t_u8 *)dest;
	while (n--)
		d[n] = s[n];
	return (dest);
}
