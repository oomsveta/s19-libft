#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	const char	*start;
	size_t		n;

	start = src;
	n = dstsize;
	if (n)
		while (--n)
			if (!(*dst++ = *src++))
				break ;
	if (!n)
	{
		if (dstsize)
			*dst = '\0';
		while (*src++)
			;
	}
	return (src - start - 1);
}
