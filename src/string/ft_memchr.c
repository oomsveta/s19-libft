#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const t_u8 *ptr;

	ptr = s;
	while (n--)
		if (*ptr++ == (t_u8)c)
			return ((void *)(--ptr));
	return (NULL);
}
