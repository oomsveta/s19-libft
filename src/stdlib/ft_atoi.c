#include "libft.h"

/*
**	Converts a string to an integer.
**
**	@param nptr	The string to convert.
**	@returns	The converted value.
*/

int	ft_atoi(const char *nptr)
{
	return (lw_strtol(nptr, NULL, 10));
}
