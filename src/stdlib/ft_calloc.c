#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ret;
	size_t	rsize;

	rsize = nmemb * size;
	if (!(ret = malloc(rsize)))
		return (NULL);
	ft_bzero(ret, rsize);
	return (ret);
}
