#include "libft.h"

/*
**	Tests if the provided character is within the ascii table.
**
**	@param c	A single character.
**	@returns	1 if the character is within the ascii table, otherwise 0.
*/

int	ft_isascii(int c)
{
	return ((unsigned)(c) < 128U);
}
